package com.ittttt.provider.pojo;

public class SysRole {

    /** 主键 */
    private Long id;

    /** 角色名 */
    private String name;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
